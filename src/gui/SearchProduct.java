    private void rTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rTableMouseClicked
        int ri=rTable.getSelectedRow();
        if(ri!=-1){
            n.setText((String)rTable.getValueAt(ri,0));
            p.setText((int)rTable.getValueAt(ri,1) +"");
            q.setText((int)rTable.getValueAt(ri,2) +"");
            d.setDate((java.util.Date)rTable.getValueAt(ri,3));
        }
    }//GEN-LAST:event_rTableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // update product
        try{
            int ri=rTable.getSelectedRow();
            if(ri!=-1){
                String old_name=(String)rTable.getValueAt(ri,0);
                String name=n.getText();
                String price=p.getText();
                String qty=q.getText();
                java.util.Date dt=d.getDate();

                int pr=Integer.parseInt(price);
                int quantity=Integer.parseInt(qty);
                java.sql.Date date=new java.sql.Date(dt.getTime());

                //database code
                db.DbConnect.st.executeUpdate("update product set name='"+name+"' , price="+pr+" , qty="+quantity+" , expiry='"+date+"' where name='"+old_name+"'");
                JOptionPane.showMessageDialog(null, "Success");
                
                getEntries();
                
                n.setText(null);
                p.setText(null);
                q.setText(null);
                d.setDate(null);
            }
        }catch(java.sql.SQLIntegrityConstraintViolationException ex){
            JOptionPane.showMessageDialog(null, "Already Exist");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // delete product
        int r=JOptionPane.showConfirmDialog(null, "Do u really wanna delete?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if(r==JOptionPane.YES_OPTION){
            try{
                int ri=rTable.getSelectedRow();
                if(ri!=-1){
                    String name=(String)rTable.getValueAt(ri,0);

                    //database code
                    db.DbConnect.st.executeUpdate("delete from product where name='"+name+"'");
                    JOptionPane.showMessageDialog(null, "Success");

                    getEntries();

                    n.setText(null);
                    p.setText(null);
                    q.setText(null);
                    d.setDate(null);
                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

