    private void getEntries(){
        javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)rTable.getModel();
        int rc=dtm.getRowCount();
        while(rc--!=0){
            dtm.removeRow(0);
        }
        String s=n.getText();
        try{
            ResultSet rs=db.DbConnect.st.executeQuery("select * from product where name like '%"+s+"%' and expiry>now() and qty>0");
            while(rs.next()){
                Vector r=new Vector();
                r.add(rs.getString("name"));
                r.add(rs.getInt("price"));
                r.add(rs.getInt("qty"));
                dtm.addRow(r);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex); 
        }
    }
    
    private void getTotalAmount(){
        int rc=sTable.getRowCount();
        int total=0;
        for(int x=0;x<rc;x++){
            total += (int)sTable.getValueAt(x, 3);
        }
        ta.setText(total+"");
    }
