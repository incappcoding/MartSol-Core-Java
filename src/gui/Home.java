    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //add to cart
        int ri=rTable.getSelectedRow();
        if(ri!=-1){
            String name=(String)rTable.getValueAt(ri,0);
            int price=(int)rTable.getValueAt(ri,1);
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)sTable.getModel();
            int rc=dtm.getRowCount();
            boolean flag=false;
            int index=0;
            for(int x=0;x<rc;x++){
                String n=(String)sTable.getValueAt(x,0);
                if(name.equals(n)){
                    flag=true;
                    index=x;
                    break;
                }
            }
            if(flag){
                int q=(int)sTable.getValueAt(index,2);
                q++;
                sTable.setValueAt(q, index, 2);
                int p=(int)sTable.getValueAt(index,1);
                sTable.setValueAt(p*q, index, 3);
            }else{
                Vector r=new Vector();
                r.add(rTable.getValueAt(ri,0));
                r.add(rTable.getValueAt(ri,1));
                r.add(1);
                r.add(rTable.getValueAt(ri,1));
                dtm.addRow(r);
            }
            try{
                db.DbConnect.st.executeUpdate("update product set qty=qty-1 where name='"+name+"'");
                getEntries();
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null,ex); 
            }
            getTotalAmount();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

